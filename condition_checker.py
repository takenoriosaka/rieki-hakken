"""
商品状態チェッカー

仕入れ候補のタイトルから状態を判定し、
メルカリ販売推定価格への補正係数を返す。

メルカリ中央値の基準状態 = 「良品・AB品相当（目立った傷なし）」= 1.00x

補正係数の考え方:
  - 新品は中古より大幅に高く売れる（+30%程度）
  - 美品・極美品はやや高く売れる（+5〜15%）
  - 傷・汚れありは安く売らないと売れない（-20〜60%）
  - 状態記載なし = 不明 → 保守的に-10%
"""

from dataclasses import dataclass

@dataclass
class ConditionResult:
    label: str    # 表示用ラベル（例: "美品", "傷あり"）
    factor: float # 価格補正係数（中央値に掛ける）
    rank: int     # 並び替え用ランク（高いほど良い状態）


# 状態判定テーブル（優先度順 = リストの上から評価）
_CONDITION_TABLE: list[tuple[list[str], ConditionResult]] = [
    # ── 新品・未使用 ──
    (
        ["新品未使用", "新品・未使用", "未使用品", "デッドストック"],
        ConditionResult("新品・未使用", 1.30, 10),
    ),
    (
        ["新品", "未使用", "タグ付き", "タグ有り", "未着用", "未開封"],
        ConditionResult("新品・未使用", 1.30, 10),
    ),
    # ── 未使用に近い / 極美品 ──
    (
        ["未使用に近い", "ほぼ未使用", "ほぼ新品", "新品同様", "極美品", "超美品", "SA品", "Sランク"],
        ConditionResult("未使用に近い", 1.15, 8),
    ),
    # ── 美品 ──
    (
        ["美品", "Aランク", "A品", "ほぼ美品", "ほぼA品"],
        ConditionResult("美品", 1.05, 7),
    ),
    # ── 良品 / AB品（基準） ──
    (
        ["良品", "AB品", "ABランク", "AB品相当", "目立った傷や汚れなし", "目立つ傷なし",
         "目立った傷なし", "状態良好", "綺麗"],
        ConditionResult("良品", 1.00, 6),
    ),
    # ── B品 / 使用感あり ──
    (
        ["B品", "Bランク", "使用感あり", "使用感有り", "使用感少々",
         "経年", "ヴィンテージ感", "アンティーク感"],
        ConditionResult("使用感あり", 0.80, 4),
    ),
    # ── やや傷・汚れ ──
    (
        ["やや傷", "やや汚れ", "小傷", "薄い傷", "若干の傷", "若干傷",
         "C品", "Cランク", "BC品", "BCランク"],
        ConditionResult("やや傷・汚れ", 0.70, 3),
    ),
    # ── 傷・汚れあり ──
    (
        ["傷あり", "傷有り", "汚れあり", "汚れ有り", "傷・汚れあり", "傷汚れあり",
         "傷や汚れ", "使用感多"],
        ConditionResult("傷・汚れあり", 0.60, 2),
    ),
    # ── 難あり / ジャンク ──
    (
        ["難あり", "難有り", "訳あり", "ジャンク", "D品", "Dランク",
         "要修理", "修理必要", "破損", "汚損", "傷多数", "大きな傷"],
        ConditionResult("難あり", 0.40, 1),
    ),
]

# 状態記載なしのデフォルト（保守的に-10%）
_DEFAULT = ConditionResult("状態不明", 0.90, 5)


def check(title: str) -> ConditionResult:
    """
    商品タイトルから状態を判定して補正係数を返す。
    複数ワードが一致した場合は最初にマッチしたもの（優先度高）を採用。
    """
    for keywords, result in _CONDITION_TABLE:
        if any(kw in title for kw in keywords):
            return result
    return _DEFAULT
